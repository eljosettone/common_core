#include "ft_printf.h"

int	ft_putnbr(long nb)
{
	if (nb < 0)
	{
		write (1, "-", 1);
		nb *= -1;
	}
	if (nb >= 0 && nb <= 9)
		write (1, ((char)nb + '0'), 1);
	else
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}